@Composable
fun TopicGrid(modifier: Modifier = Modifier) {

    LazyColumn(
        modifier = Modifier
            .background(Color.White)
            .fillMaxWidth(), //A coluna deve ocupar toda a largura disponível do contêiner pai(tela).

    ) {
        item {
            //CABEÇALHO
            Row(
                Modifier
                    .padding(8.dp)
                    .fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceBetween // Alinha os itens de forma que haja espaço entre eles

            ) {
                Icon(
                    imageVector = Icons.Filled.Menu,
                    contentDescription = "Menu Icon",//O ícone faz parte do conjunto de ícones disponíveis  pelo Jetpack Compose na biblioteca material3.
                    modifier = Modifier.size(60.dp)//AUMENTANDO O TAMANHO DO ÍCONE
                )
                Spacer(modifier = Modifier.width(55.dp)) // Espaço entre o ícone e o texto do cabeçalho

                Text(
                    text = "CIN",
                    style = TextStyle(
                        fontFamily = FontFamily.Default,
                        fontWeight = FontWeight.Bold,
                        fontSize = 50.sp,
                        color = Color.Red,


                        )
                )

                Text(
                    text = "ETEC",
                    style = TextStyle(
                        fontFamily = FontFamily.Default,
                        fontWeight = FontWeight.Bold,
                        fontSize = 50.sp,
                        color = Color.Black,


                        )
                )
                Spacer(modifier = Modifier.width(40.dp)) // Espaço flexível para empurrar o próximo ícone para a direita
//Segundo icone
                Icon(
                    imageVector = Icons.Filled.AccountCircle, contentDescription = "Account Icon",
                    modifier = Modifier.size(60.dp),
                )
            }
        }
    }
        LazyVerticalGrid(
        columns = GridCells.Fixed(2),
        verticalArrangement = Arrangement.spacedBy(dimensionResource(R.dimen.padding_small)),
        horizontalArrangement = Arrangement.spacedBy(dimensionResource(R.dimen.padding_small)),
        modifier = modifier
            .padding(top  = 80.dp )
    ) {
        items(DataSource.topics) { topic ->
            TopicCard(topic)
        }
    }
}//fim da fun


@Composable
fun TopicCard(topic: Topic, modifier: Modifier = Modifier) {
    Card {
        Row {
            Box {
                Image(
                    painter = painterResource(id = topic.imageRes),
                    contentDescription = null,
                    modifier = modifier
                        .size(width = 68.dp, height = 68.dp)
                        .aspectRatio(1f),
                    contentScale = ContentScale.Crop
                )
            }

            Column {
                Text(
                    text = stringResource(id = topic.name),
                    style = MaterialTheme.typography.bodyMedium,
                    modifier = Modifier.padding(
                        start = dimensionResource(R.dimen.padding_medium),
                        top = dimensionResource(R.dimen.padding_medium),
                        end = dimensionResource(R.dimen.padding_medium),
                        bottom = dimensionResource(R.dimen.padding_small)
                    )
                )
                Row(verticalAlignment = Alignment.CenterVertically) {
                    Icon(
                        painter = painterResource(R.drawable.ic_grain),
                        contentDescription = null,
                        modifier = Modifier
                            .padding(start = dimensionResource(R.dimen.padding_medium))
                    )
                    Text(
                        text = topic.availableCourses.toString(),
                        style = MaterialTheme.typography.labelMedium,
                        modifier = Modifier.padding(start = dimensionResource(R.dimen.padding_small))
                    )
                }
            }
        }
    }
}

